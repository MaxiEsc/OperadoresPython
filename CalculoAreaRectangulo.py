# EJERCICIO PARA CALCULAR EL AREA DE UN TRIANGULO
print('**** CALCULAR EL AREA DE UN TRIANGULO ****')
base = float(input("Ingrese la base: > "))
altura = float(input('ingrese la altura: > '))

area = base * altura
perimetro = 2 * (base + altura)
print(f'''El Area de un rectangulo es: {area:.2f}
El perimetro de un Rectangulo es: {perimetro:.2f}
muchas gracias por utilizar nuestros servicios''')

