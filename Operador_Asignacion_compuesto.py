#Operador de asignacion compuesto
a,b = 20, 30
print("Valoe inicial de: a={}, b={}".format(a,b))
#operador Compuesto suma
a += b
print("Operador a += b: {}".format(a))
#operador Compuesto Resta
a,b = 20, 30
a -= b
print("Operador a -= b: {}".format(a))
#operador Compuesto Multiplicacion
a,b = 20, 30
a *= b
print("Operador a *= b: {}".format(a))
#operador Compuesto Modulo
a,b = 20, 30
a %= b
print("Operador a %= b: {}".format(a))
#operador Compuesto Potencia
a,b = 20, 30
a **= b
print("Operador a **= b: {}".format(a))
#operador Compuesto Division
a,b = 20, 30
a /= b
print("Operador a /= b: {}".format(a))
#operador Compuesto Division Entera
a,b = 20, 30
a //= b
print("Operador a //= b: {}".format(a))
