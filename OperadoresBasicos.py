#Operadores aritmetico
a = 20
b = 7
#Suma
suma = a + b
print("Suma: {}".format(suma))
#Resta
resta = a - b
print("Resta: {}".format(resta))
#Multiplicacion
multiplicacion = a * b
print("Multiplicacion: {}".format(multiplicacion))
#Division
division = a / b
print("Division: {}".format(division))
#Division Entera
division_entera = a // b
print("Division Entera: {}".format(division_entera))
#Potencia
potencia = a ** b
print("Potencia: {}".format(potencia))
#Modulo
modulo = a % b
print("Modulo: {}".format(modulo))
