#Operadores Condicionales
a, b = 10, 15
print("Valor iniciales a = {} | b = {}".format(a,b))
print("Valor de a==B es: {}".format(a==b))
print("Valor de a!=B es: {}".format(a!=b))
print("Valor de a<B es: {}".format(a<b))
print("Valor de a>B es: {}".format(a>b))
print("Valor de a>=B es: {}".format(a>=b))
print("Valor de a<=B es: {}".format(a<=b))
